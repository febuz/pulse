def _make_web() -> tuple[Web, list[tuple[str, str, str]]]:
    """Seed the chemistry Web; return (web, [(label, equation, cid), …])."""
    h2  = Species.make("H2",      {"H": 2})
    h2o = Species.make("H2O",     {"H": 2, "O": 1})

    n2      = Species.make("N2",       {"N": 2})
    nh3     = Species.make("NH3",      {"N": 1, "H": 3})
    co2     = Species.make("CO2",      {"C": 1, "O": 2})
    acetic  = Species.make("CH3COOH", {"C": 2, "H": 4, "O": 2})
    v2o5    = Species.make("V2O5",     {"V": 2, "O": 5})
    v2o4    = Species.make("V2O4",     {"V": 2, "O": 4})
    h2so4   = Species.make("H2SO4",    {"H": 2, "S": 1, "O": 4})
    voso4   = Species.make("VOSO4",    {"V": 1, "O": 5, "S": 1})

    reactions: list[tuple[str, Reaction]] = [
        ("Haber-Bosch NH₃ synthesis",         Reaction((Term(n2,1), Term(h2,3)),(Term(nh3,2),))),
        ("CO₂ hydrogenation → acetic acid",   Reaction((Term(co2,2), Term(h2,4)),(Term(acetic,1), Term(h2o,2)))),
        ("V₂O₅ reduction → V₂O₄ (VRFB)",     Reaction((Term(v2o5,1), Term(h2,1)),(Term(v2o4,1), Term(h2o,1)))),
        ("Vanadyl sulfate preparation",        Reaction((Term(v2o4,1), Term(h2so4,2)),(Term(voso4,2), Term(h2o,2)))),
    ]

    ck  = ChemistryKnitweb(_PRIV)
    web = Web()
    seeded: list[tuple[str, str, str]] = []
    for label, rxn in reactions:
        assert is_balanced(rxn), label
        cid, att = ck.weave(rxn, web)
        eq = att.record.get("equation", "")
        seeded.append((label, eq, cid))
    return web, seeded


def _lens_retrieve(web: Web) -> dict[str, dict]:
    queries = [
        {"kind": "reaction-knowledge", "text": "VOSO4"},
        {"kind": "reaction-knowledge", "text": "V2O5"},
        {"kind": "reaction-knowledge", "text": "NH3"},
        {"kind": "reaction-knowledge", "text": "CH3COOH"},
    ]
    records: dict[str, dict] = {}
    for q in queries:
        cs = retrieve(q, ["reaction-knowledge"], web, depth=2)
        records.update(cs.records(web))
    return records


def _token_savings(records: dict[str, dict]) -> tuple[int, int, float]:
    full  = json.dumps(list(records.values()), indent=2)
    slim  = json.dumps([{"equation": r.get("equation",""), "kind": r.get("kind","")} for r in records.values()])
    n, l  = max(1, len(full)//4), max(1, len(slim)//4)
    return n, l, 100*(1-l/n)


def _synthesise(records: dict[str, dict]) -> str:
    import re
    lines = [
        '"""Auto-generated by Knitweb Chemistry Lens Agent.\n'
        'Reactions retrieved from the P2P fabric via Lens (interpret.retrieve)\n'
        'and validated against the ChemistryKnitweb conservation gate.\n"""\n',
        "from knitweb.knitwebs.chemistry import ChemistryKnitweb, Reaction, Species, Term, is_balanced\n\n",
        "DEMO_PRIV = '0' * 64  # replace with real secp256k1 key\n",
        "_ck = ChemistryKnitweb(DEMO_PRIV)\n\n",
    ]
    for record in records.values():
        eq  = record.get("equation","")
        slug = re.sub(r"[^a-z0-9]+","_", eq.lower())[:36].strip("_")
        rts = record.get("reactants",[])
        pts = record.get("products",[])
        def v(t): return re.sub(r"[^a-zA-Z0-9_]","_",t["species"].lower())
        seen = {}
        defs = []
        for t in rts+pts:
            vn = v(t)
            if vn not in seen:
                seen[vn]=1
                comp = {e:c for e,c in t["composition"]}
                defs.append(f"    {vn} = Species.make({t['species']!r}, {comp!r}, charge={t['charge']})")
        r_t = ", ".join(f"Term({v(t)},{t['coeff']})" for t in rts)
        p_t = ", ".join(f"Term({v(t)},{t['coeff']})" for t in pts)
        lines.append(
            f"def reaction_{slug}() -> Reaction:\n"
            f'    """{eq}"""\n'
            + "\n".join(defs) + "\n"
            f"    rxn = Reaction(({r_t}),({p_t}))\n"
            f"    assert is_balanced(rxn)\n"
            f"    return rxn\n\n"
        )
    return "".join(lines)


def _push_github(content: str) -> str:
    token = os.environ.get("GITHUB_TOKEN","")
    repo  = os.environ.get("GITHUB_REPO","")
    path  = "generated/chemistry_reactions.py"
    if not token or not repo:
        return f"DRY RUN — would push {len(content)} bytes to {path}"
    import urllib.request
    api_url = f"https://api.github.com/repos/{repo}/contents/{path}"
    headers = {"Authorization": f"Bearer {token}", "Accept": "application/vnd.github+json",
                "Content-Type": "application/json"}
    sha = None
    try:
        req = urllib.request.Request(api_url, headers=headers)
        with urllib.request.urlopen(req, timeout=8) as r:
            sha = json.loads(r.read()).get("sha")
    except Exception:
        pass
    payload: dict = {"message": "chore: chemistry reactions from Lens agent", "content": b64encode(content.encode()).decode()}
    if sha:
        payload["sha"] = sha
    req = urllib.request.Request(api_url, json.dumps(payload).encode(), headers=headers, method="PUT")
    with urllib.request.urlopen(req, timeout=15) as r:
        url = json.loads(r.read()).get("content",{}).get("html_url","")
        return f"Pushed → {url}"


