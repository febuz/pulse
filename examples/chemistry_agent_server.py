def _sse(event: str, data: dict) -> str:
    return f"event: {event}\ndata: {json.dumps(data)}\n\n"


async def run_agent() -> AsyncGenerator[str, None]:
    """Yield SSE events for each agent step."""
    await asyncio.sleep(0.1)

    # Step 1: seed
    yield _sse("step", {"n": 1, "label": "Seeding chemistry knowledge graph (P2P spider role)"})
    t0 = time.monotonic()
    web, seeded = _make_web()
    for label, eq, cid in seeded:
        yield _sse("reaction_seeded", {"label": label, "equation": eq, "cid": cid[:22]+"…"})
        await asyncio.sleep(0.15)
    yield _sse("info", {"text": f"{len(seeded)} reactions woven into Web node (5mart.ml relay in production)"})
    await asyncio.sleep(0.3)

    # Step 2: Lens retrieval
    yield _sse("step", {"n": 2, "label": "Lens retrieval via interpret.retrieve — querying P2P fabric"})
    records = _lens_retrieve(web)
    for cid, rec in records.items():
        yield _sse("reaction_retrieved", {
            "equation": rec.get("equation",""),
            "cid": cid[:22]+"…",
            "kind": rec.get("kind",""),
        })
        await asyncio.sleep(0.18)
    yield _sse("info", {"text": f"Total unique records retrieved: {len(records)}"})
    await asyncio.sleep(0.3)

    # Step 3: token savings
    yield _sse("step", {"n": 3, "label": "Token savings — Lens distillation vs naive context"})
    naive, lens_t, pct = _token_savings(records)
    yield _sse("savings", {"naive": naive, "lens": lens_t, "pct_saved": round(pct, 1)})
    await asyncio.sleep(0.3)

    # Step 4: code synthesis
    yield _sse("step", {"n": 4, "label": "Synthesising typed Python module from retrieved reactions"})
    module = _synthesise(records)
    lines  = module.count("\n")
    yield _sse("code_generated", {"lines": lines, "chars": len(module), "preview": module[:300]})
    await asyncio.sleep(0.3)

    # Step 5: GitHub push
    yield _sse("step", {"n": 5, "label": "Pushing generated code to GitHub"})
    result = _push_github(module)
    yield _sse("github_push", {"result": result})
    await asyncio.sleep(0.2)

    elapsed = round(time.monotonic() - t0, 3)
    yield _sse("done", {
        "elapsed_s": elapsed,
        "reactions_seeded": len(seeded),
        "records_retrieved": len(records),
        "pct_saved": round(pct, 1),
        "code_lines": lines,
    })


